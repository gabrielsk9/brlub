      -- C8O7DN_135098_PR_PD.dbo.AT_MICHELIN_B2CUSTO2016(B1_COD, B2_LOCAL, B2_FILIAL) NEW_CUSTOB2_MICHELIN,
       B1_CABO AS SEGMENTO_PRODUTO
