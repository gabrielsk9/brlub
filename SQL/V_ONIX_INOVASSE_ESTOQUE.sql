          --Round(B1_CUSTD + (B1_CUSTD * ((B1_PPIS + B1_PCOFINS) / 100)), 2)
		  Round(B1_CUSTD * CASE WHEN B1_DESC LIKE 'LT LUST%' THEN 0.94 ELSE 1 END ,2) 
       END CUSTO_STD,
       CASE
         WHEN B2_CM1 <> 0 THEN
          --Round(B2_CM1 + (B2_CM1 * ((B1_PPIS + B1_PCOFINS) / 100)), 2)
		  Round(B2_CM1 * CASE WHEN B1_DESC LIKE 'LT LUST%' THEN 0.94 ELSE 1 END ,2) 
