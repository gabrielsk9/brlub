         -- Round(B1_CUSTD + (B1_CUSTD * ((B1_PPIS + B1_PCOFINS) / 100)), 2)
		 Round(B1_CUSTD * CASE WHEN B1_DESC LIKE 'LT LUST%' THEN 0.94 ELSE 1 END ,2) 
       END CUSTO_CMV,
       ISNULL((SELECT SUM(C6_QTDVEN - C6_QTDENT)
             FROM C8O7DN_135098_PR_PD.dbo.SC6140 C6, C8O7DN_135098_PR_PD.dbo.SF4140 F4
            WHERE F4.D_E_L_E_T_ = ' '
              AND F4_ESTOQUE = 'S'
              AND F4_CODIGO = C6_TES
              AND F4_FILIAL = C6_FILIAL
              AND C6.D_E_L_E_T_ = ' '
              AND C6_LOCAL = B2_LOCAL
              AND C6_PRODUTO = B1_COD
              AND C6_BLQ <> 'R'
              AND C6_FILIAL = B2_FILIAL),
